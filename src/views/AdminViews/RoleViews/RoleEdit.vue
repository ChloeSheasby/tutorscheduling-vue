
<template>
  <div>
    <v-container>
      <v-toolbar>
        <v-toolbar-title>{{ this.message}}</v-toolbar-title>
      </v-toolbar>
      <br>
    <v-form
      ref="form" 
      v-model="valid"
      lazy validation
    >
      <v-text-field
        v-model="role.type"
        id="type"
        :counter="50"
        label="Name"
        required
      ></v-text-field>
    
      <v-select
        v-model="role.groupId"
        :items="groups"
        item-text="name"
        item-value="id"
        label="Group"
        required
      >
      </v-select>

      <v-btn
        :disabled="!valid"
        color="success"
        class="mr-4"
        @click="updateRole"
      >
        Save
      </v-btn>

      <v-btn
        color="error"
        class="mr-4"
        @click="cancel"
      >
        Cancel
      </v-btn>
    </v-form>
    </v-container>
  </div>
</template>

<script>
import RoleServices from "@/services/roleServices.js";
import GroupServices from "@/services/groupServices.js";

export default {
  props: ["id"],

  data() {
    return {
      valid: false,
      role: {},
      group: {},
      groups: [],
      message: "Edit Role - make updates to the fields and click Save",
        roles: [
        'admin'
      ],
    };
  },
  async created() {
    await RoleServices.getRole(this.id)
      .then((response) => {
        this.role = response.data;
      })
      .catch((error) => {
        console.log("There was an error:", error.response);
      }),
    GroupServices.getGroup(this.role.id)
      .then((response) => {
        this.group = response.data;
      })
      .catch((error) => {
        this.message = error.response.data.message
        console.log("There was an error:", error.response);
      }),
    GroupServices.getAllGroups()
        .then((response) => {
          this.groups = response.data;
        })
        .catch((error) => {
          this.message = error.response.data.message
          console.log("There was an error:", error.response);
      });
  },

  methods: {
    // only updating this replace issue once edits can be done on view page
    // also this.id is role id
    updateRole() {
      RoleServices.updateRole(this.id, this.role)
        .then(() => {
          this.$router.go(-1);
        })
        .catch((error) => {
          this.message = error.response.data.message
          console.log(error);
        });
    },
    cancel() {
      this.$router.go(-1);
    },
  },
};
</script>

